package org.firstinspires.ftc.teamcode.Utils;

/**
 * Central runtime configuration for the robot code.
 */
public class RobotConfig {
	/**
	 * When {@code true}, the robot is in competition mode and all telemetry
	 * (Panels dashboard + driver station) is suppressed. Flip to {@code false}
	 * for testing/debugging to restore full telemetry output.
	 */
	public static final boolean COMPETITION = false;
}
