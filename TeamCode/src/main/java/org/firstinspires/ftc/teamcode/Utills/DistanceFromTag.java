package org.firstinspires.ftc.teamcode.Utills;

public class DistanceFromTag {
    int tag;
    double distance;

    public DistanceFromTag(int tag, double distance) {
        this.tag = tag;
        this.distance = distance;
    }

    public int getTag() {
        return tag;
    }

    public double getDistance() {
        return distance;
    }
}